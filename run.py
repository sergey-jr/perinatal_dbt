def main_statics(files):
    for year in files:
        for i, file_path in enumerate(files[year]):
            df = pd.read_excel(file_path, sheet_name='t1_1')
            df.columns = ['region', 'birth', 'd1', 'd2', 'd3', 'death', 'd4', 'd5', 'd6', 'death_children', 'd7', 'd8',
                          'd9', 'inc_decr', 'd10', 'marriage', 'd11', 'd12', 'd13', 'divorce', 'd14', 'd15', 'd16']
            df = df[df.region.isin(regions)]
            df = df[['region', 'birth', 'death_children', 'marriage', 'divorce']]
            df.insert(5, "year", year)
            month = f'0{i + 1}' if i < 9 else str(i + 1)
            df.insert(6, "month", month)
            file = open(f'data/main/{year}/{month}.csv', mode='w')
            file.close()
            df.to_csv(f'data/main/{year}/{month}.csv')


cwd = os.getcwd()
files = {f"{y}": [] for y in range(2012, 2019)}
for r, d, f in os.walk(os.path.join(cwd, 'row_data')):
    for file in f:
        if '.xls' in file and '~' not in file:
            path = os.path.join(r, file)
            year = re.findall(r'\d{4}', file)[0]
            if year in files.keys():
                files[year].append(path)
main_statics(files)
